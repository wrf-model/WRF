      END DO 
      RETURN 
!                                                                       
!     Code for equal, positive, non-unit increments.                    
!                                                                       
   60 NS = N*incX 
      DO i = 1,NS,incX 
        radm2_WDOT = radm2_WDOT + DX(i)*DY(i) 
      END DO 

